export function LanguageProvider({ children }: { children: ReactNode }) {
  // Hydrate language and theme from localStorage, default to 'en' and 'light'
  const [lang, setLangState] = useState<Lang>(() => {
    const stored = localStorage.getItem('simba-lang') as Lang | null;
    return stored && SUPPORTED_LANGS.includes(stored) ? stored : 'en';
  });
  const [theme, setThemeState] = useState<Theme>(() => (localStorage.getItem('simba-theme') as Theme) || 'light');

  const setTheme = useCallback((t: Theme) => {
    localStorage.setItem('simba-theme', t);
    setThemeState(t);
  }, []);

  const setLang = useCallback((next: Lang) => {
    localStorage.setItem('simba-lang', next);
    setLangState(next);
  }, []);

  const toggleLang = useCallback(() => {
    setLangState(prev => {
      const next = prev === 'en' ? 'ar' : 'en';
      localStorage.setItem('simba-lang', next);
      return next;
    });
  }, []);

  const toggleTheme = useCallback(() => {
    setThemeState(prev => {
      const next = prev === 'light' ? 'dark' : 'light';
      localStorage.setItem('simba-theme', next);
      return next;
    });
  }, []);

  /**
   * Translates a dot-path key (e.g. "nav.home") into the current language's string.
   * Supports {param} interpolation: t('interactions.minutesAgo', { n: '5' }) → "5m ago"
   * Returns the raw path if the key is not found (useful for debugging missing translations).
   */
  const t = useCallback((path: string, params?: Record<string, string>): string => {
    const keys = path.split('.');
    const lookup = (dict: Record<string, unknown>): unknown => {
      let cur: unknown = dict;
      for (const key of keys) {
        cur = (cur as Record<string, unknown>)?.[key];
      }
      return cur;
    };
    // Try current lang first; fall back to English for missing keys.
    let result = lookup(translations[lang]);
    if (result === undefined || result === null) result = lookup(translations.en);
    if (result === undefined || result === null) return path;
    if (params && typeof result === 'string') {
      return Object.entries(params).reduce(
        (str, [k, v]) => str.replace(`{${k}}`, v),
        result
      );
    }
    return result as string;
  }, [lang]);

  // Set HTML dir and lang attributes for RTL support
  useEffect(() => {
    const html = document.documentElement;
    html.setAttribute('dir', RTL_LANGS.includes(lang) ? 'rtl' : 'ltr');
    html.setAttribute('lang', lang);
  }, [lang]);

  // Toggle Tailwind dark class on <html> for dark mode
  useEffect(() => {
    document.documentElement.classList.toggle('dark', theme === 'dark');
  }, [theme]);

  const value = useMemo(() => ({
    lang, setLang, toggleLang, t, theme, setTheme, toggleTheme,
  }), [lang, setLang, toggleLang, t, theme, setTheme, toggleTheme]);

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
}

/**
 * Hook to access language/theme state and the t() translation function.
 * Must be used within a LanguageProvider — throws if not.
 */
export const useLang = () => {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error('useLang must be used within LanguageProvider');
  return ctx;
};
